/*
 * 变量其实只不过是程序可操作的存储区的名称。
 * C 中每个变量都有特定的类型，类型决定了变量存储的大小和布局，该范围内的值都可以存储在内存中，运算符可应用于变量上。
 * 变量的名称可以由字母、数字和下划线字符组成。它必须以字母或下划线开头。大写字母和小写字母是不同的，因为 C 是大小写敏感的。
 *
 * 单精度浮点值。单精度是这样的格式，1位符号，8位指数，23位小数。
 * 双精度浮点值。双精度是1位符号，11位指数，52位小数。
 *
 * 定义： 就是告诉编译器在何处创建变量的存储，以及如何创建变量的存储。变量定义指定一个数据类型，并包含了该类型的一个或多个变量的列表，如下所示：
 *          type variable_list;
 *        在这里，type 必须是一个有效的 C 数据类型，可以是 char、w_char、int、float、double 或任何用户自定义的对象，variable_list 可以由一个或多个标识符名称组成，多个标识符之间用逗号分隔。
 *
 * C语言变量声明:
 *      变量声明向编译器保证变量以指定的类型和名称存在，这样编译器在不需要知道变量完整细节的情况下也能继续进一步的编译。变量声明只在编译时有它的意义，在程序连接时编译器需要实际的变量声明。
 *      变量的声明有两种情况：
 *          1、一种是需要建立存储空间的。例如：int a 在声明的时候就已经建立了存储空间。
 *          2、另一种是不需要建立存储空间的，通过使用extern关键字声明变量名而不定义它。 例如：extern int a 其中变量 a 可以在别的文件中定义的。除非有extern关键字，否则都是变量的定义。
 *
 */

