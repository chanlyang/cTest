#include "stdio.h"

//在函数外部定义i和j
int i;
int j;

int addTowNum() {
    //在函数内部声明变量i和j为外部变量
    extern int i;
    extern int j;
    //给外部变量（全局变量）赋值
    i = 2;
    j = 4;
    return i + j;
}

